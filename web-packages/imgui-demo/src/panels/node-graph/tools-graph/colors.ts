  static toRGBA (color: number): [number, number, number, number] {
    return [
      (color >> 16) & 0xFF,
      (color >> 8) & 0xFF,
      color & 0xFF,
      (color >> 24) & 0xFF,
    ];
  }

  static fromRGBA (r: number, g: number, b: number, a: number = 255): number {
    return ((a & 0xFF) << 24) | ((r & 0xFF) << 16) | ((g & 0xFF) << 8) | (b & 0xFF);
  }
